export default function ActionBar({
  running,
  onStart,
  onPause,
  onReset,
  onOpenSettings,
}) {
  return (
    <div className="actions">
      {!running ? (
        <button className="pill pill-dark pill-glow" onClick={onStart}>
          <PlayIcon />
          <span>Start</span>
        </button>
      ) : (
        <button className="pill pill-dark pill-glow" onClick={onPause}>
          <PauseIcon />
          <span>Pause</span>
        </button>
      )}

      <button className="pill pill-light" onClick={onReset}>
        <ResetIcon />
        <span>Reset</span>
      </button>

      <button className="icon-btn" onClick={onOpenSettings} aria-label="Settings">
        <GearIcon />
      </button>
    </div>
  );
}

